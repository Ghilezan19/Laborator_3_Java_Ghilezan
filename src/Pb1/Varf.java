package Pb1;

public class Varf {

    private double x, y;

    public Varf(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Varf() {}

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
